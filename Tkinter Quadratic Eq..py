from math import*
import cmath
from tkinter import*
m=Tk()
l1=Label(m,text="Enter a:").grid(row=0,column=0,padx=10,pady=10)
l2=Label(m,text="Enter b:").grid(row=1,column=0,padx=10,pady=10)
l3=Label(m,text="Enter c:").grid(row=2,column=0,padx=10,pady=10)
e1=Entry(m,width=20)
e1.grid(row=0,column=1,padx=10,pady=10)
e2=Entry(m,width=20)
e2.grid(row=1,column=1,padx=10,pady=10)
e3=Entry(m,width=20)
e3.grid(row=2,column=1,padx=10,pady=10)
e4=Entry(m,width=20)
e4.grid(row=3,column=0,padx=10,pady=10)
e5=Entry(m,width=20)
e5.grid(row=3,column=1,padx=10,pady=10)
def quad():
    a=int(e1.get())
    b=int(e2.get())
    c=int(e3.get())
    d=cmath.sqrt((b**2)-4*a*c)
    s1=((-b+d)/(2*a))
    s2=((+b+d)/2*a)
    e4.insert(0,s1)
    e4.delete(0,1)
    e5.insert(0,s2)
    e5.delete(0,1)
b1=Button(m,text="Calculate",bg="pink",fg="red",command=quad).grid(row=4,column=0,pady=10,padx=10)
m.mainloop()